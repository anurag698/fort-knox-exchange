import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: "default" | "glass" | "elevated" }
>(({ className, variant = "default", ...props }, ref) => {
  const variantStyles = {
    default: "rounded-lg border bg-card text-card-foreground shadow-sm",
    glass: "glass rounded-xl text-card-foreground shadow-lg hover:shadow-xl transition-smooth",
    elevated: "rounded-xl border bg-card text-card-foreground shadow-md hover:shadow-xl hover:-translate-y-1 transition-smooth"
  }

  return (
    <div
      ref={ref}
      className={cn(variantStyles[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

